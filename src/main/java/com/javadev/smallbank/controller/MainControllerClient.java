package com.javadev.smallbank.controller;

import com.javadev.smallbank.entity.Client;
import com.javadev.smallbank.repository.ClientRepository;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Controller;
import org.springframework.web.bind.annotation.*;

import java.util.Map;

@Controller    // This means that this class is a Controller
@RequestMapping(path="/cl") // This means URL's start with /cl (after Application path)
public class MainControllerClient {
    @Autowired // This means to get the bean called userRepository
    // Which is auto-generated by Spring, we will use it to handle the data
    private ClientRepository clientRepository;

    //


    @RequestMapping("/index")           //    Logically coincides with -> @GetMapping(path="/all")
    public String listContacts(Map<String, Object> map) {

        map.put("client", new Client());
        map.put("clientList", clientRepository.findAll());

        return "client";
    }

    @RequestMapping("/")
    public String home() {
        return "From class MainControllerClient";
//        return "redirect:/index";
    }

    //

    @GetMapping(path="/add") // Map ONLY GET Requests
    public @ResponseBody
    String addNewClient (@RequestParam String firstName,
                         @RequestParam String lastName,
                         @RequestParam String address,
                         @RequestParam int age) {
        // @ResponseBody means the returned String is the response, not a view name
        // @RequestParam means it is a parameter from the GET or POST request

        Client client = new Client();
        client.setFirstName(firstName);
        client.setLastName(lastName);
        client.setAddress(address);
        client.setAge(age);
        clientRepository.save(client);
        return "Saved";
    }

    @GetMapping(path="/all")
    public @ResponseBody Iterable<Client> getAllUsers() {
        // This returns a JSON or XML with the users
        return clientRepository.findAll();
    }

    @RequestMapping("/delete/{contactId}")
    public String deleteContact(@PathVariable("clientId") Integer clientId) {

        clientRepository.delete(clientId);

        return "redirect:/index";
    }

}

